			<h3 style="text-align: center;">CREATION DE WOMAN LEADER</h3>
<p>Woman leader est une ONG apolitique, globale et entièrement dirigée par des jeunes filles de diverses horizons. La première Assemblée Générale constitutive de Woman leader s’est déroulée le Mardi 10 décembre 2018 dans les locaux de l’Institut Universitaire d’Abidjan (IUA).</p>
<p>Woman leader existe pour pallier à différentes problématiques qui minent d’une façon ou d’une autre notre société. Ses différentes problématiques sont précisément liées à l’éducation et à l’autonomisation de la jeune fille. L’autonomisation de la jeune fille reste encore compliqué. Dans le monde , plus de la moitié de la population féminine est pauvre. Ce taux de pauvreté est estimé à 49%.</p>
<h3 style="text-align: center;">VISIONS DE WOMAN LEADER</h3>
<p>Woman Leader rêve d’un monde égal, juste et équitable où la jeune fille dépasse son opacité afin d’être mieux orientée, épanouie et autonome tout en exploitant son potentiel. De manière plus spécifique, aider les jeunes filles à comprendre les thématiques liées aux genres, aux féminismes , au leadership et à l’autonomisation, de sorte à ce que la jeune fille, cible première de Woman leader, puisse exploiter son plein potentiel et s’affirmer en société.</p>

